        }
        int posY = 0;
        int posx = 0;
        private void hexa_MouseMove(object sender, MouseEventArgs e)
        {
            if (e.Button != MouseButtons.Left)
            {
                posx = e.X;
                posY = e.Y;
            }
            else
            {
                Left = Left + (e.X - posx);
                Top = Top + (e.Y - posY);
            }
        }

        private void bunifuThinButton23_Click(object sender, EventArgs e)
        {
            Close();
        }

